function antallBillet2() {
    let antall = parseInt($("#antall2").html());

    $('#plus2').click(function () {
        antall += 1;
        $('#antall2').html(antall);
    });

    $('#minus2').click(function () {
        while (antall > 0) {
            antall -= 1;
        }

        $('#antall2').html(antall);

    });
};
